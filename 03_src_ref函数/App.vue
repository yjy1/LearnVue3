<template>
  <h1>一个人的信息</h1>
  <h2>姓名：{{name}}</h2>
  <h2>年龄：{{ age }}</h2>
  <h3>工作种类：{{ job.type }}</h3>
  <h3>薪水：{{ job.salary }}</h3>
  <button @click="changeInfo">修改人的信息</button>

</template>

<script>
import {ref} from 'vue'
export default {
  name: 'App',
  setup(){
    // 数据
    let name =  ref('张三') /* ref(value: string): Ref<string>
 */
    let age = ref(18)
    let job = ref({
      type:'前端工程师',
      salary:'30k'
    })
    // 方法
    function changeInfo(){
      // name = '李四'
      // age = 38
      name.value = '李四'
      age.value = 38
      // job.type
      /* 
          (property) Ref<{ type: string; salary: string; }>.value: {
             type: string;
             salary: string;
          }
      */
      // job.value = {
      //   type:'web前端工程师',
      //   salary:'35k'
      // }
      job.value.type  = 'web前端工程师'
      job.value.salary = '35k'
      console.log(job.value.type  )
    }
    
    // 返回一个对象（常用）
    return {
      name,
      age,
      job,
      changeInfo
    }
  }
}
</script>
 
